// Helper function to get translation
export const getTranslation = (key, language = 'en') => {
  const translation = translations[key]?.[language];
  if (!translation) {
    console.warn(`Translation missing for key: ${key} in language: ${language}`);
    return translations[key]?.en || key;
  }
  return translation;
};

// Helper function to get all available languages for a key
export const getAvailableLanguages = (key)=> {
  return Object.keys(translations[key] || {});
};

// Helper function to check if translation exists
export const hasTranslation = (key, language)=> {
  return !!translations[key]?.[language];
};

// Helper function for RTL languages
export const isRTL = (language) => {
  const rtlLanguages = ['ur', 'ar', 'fa', 'he'];
  return rtlLanguages.includes(language);
};

